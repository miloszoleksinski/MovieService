package com.olas.filmsinfoservice.user;

public class UserBadgeModel 
{
	private String username;
	private String badgeColor;
	private String badgeInfo;
	private String badgeTitle;
	
	public String getUsername() {return username;}
	public void setUsername(String username) {this.username = username;}
	
	public String getBadgeColor() {return badgeColor;}
	public void setBadgeColor(String badgeColor) {this.badgeColor = badgeColor;}
	
	public String getBadgeInfo() {return badgeInfo;}
	public void setBadgeInfo(String badgeInfo) {this.badgeInfo = badgeInfo;}
	
	public String getBadgeTitle() {return badgeTitle;}
	public void setBadgeTitle(String badgeTitle) {this.badgeTitle = badgeTitle;}
}
